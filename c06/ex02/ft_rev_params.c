#include<unistd.h>

int	main(int n, char **arg)
{
	int	i;
	int	j;

	i = n - 1;
	while (n > 1)
	{
		j = 0;
		while (arg[i][j])
		{
			write(1, &arg[i][j], 1);
			j ++;
		}
		i--;
		write(1, "\n", 1);
		n--;
	}
}
