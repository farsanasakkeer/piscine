#include<unistd.h>

int	main(int n, char **arg)
{
	int	i;

	i = 0;
	if (n >= 1)
	{
		while (arg[0][i])
		{
			write(1, &arg[0][i], 1);
			i ++;
		}
		write(1, "\n", 1);
	}
}
